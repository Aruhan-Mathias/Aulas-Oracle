
public class VariaveisPrimitivas {
	public static void main(String[] args) {
		//Números inteiros
		byte vByte = 120;
		short vShort = 32000;
		int vInt = 1_000_000;
		long vLong = 2_000_000;
		
		//Números ponto Flutuante 
		float vFloat = 3.14159f;
		double vDouble = 6.12321;
		
		//Guarda Verdadeiro ou falso 
		boolean vBoolean = false;
		
		//Guarda um único caractere.
		char vChar = 1;
		
	}
	
}
